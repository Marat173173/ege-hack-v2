import type { TopicDef } from "./types";
import { FIPI_RU } from "./fipi-codifier-ru";

/**
 * Строит массив из 63 этажей русского языка на основе кодификатора ФИПИ 2026.
 * Каждая подтема кодификатора = один этаж Шпиля.
 *
 * Группировка (для Тропы):
 *  - 1.x — Текст
 *  - 2.x — Функциональная стилистика
 *  - 3.1–3.8 — Язык (крупная секция)
 *  - 4.x — Общие сведения о языке
 *  - 5.x — Речь. Речевое общение
 *
 * «Боссы» (короны) — по одной в конце каждого крупного раздела:
 *   1.5, 2.5, 3.8.9, 4.4, 5.3
 */

const BOSSES = new Set(["1.5", "2.5", "3.8.9", "4.4", "5.3"]);

/**
 * Авторская карта зависимостей (методическая, не на каждую тему).
 * Инвариант: требуемая тема всегда идёт РАНЬШЕ по кодификатору,
 * поэтому циклы невозможны. Пороги: 50 — «инструментальная» база
 * (анализ как инструмент), 55 — критичная предпосылка и темы раздела
 * перед боссом.
 */
const REQUIRES: Record<string, { id: string; minProg: number }[]> = {
  // ── Боссы: корона требует тем своего раздела ──
  // «План. Тезисы. Конспект…» — итог работы с текстом.
  "1.5": [
    { id: "1.1", minProg: 55 },
    { id: "1.2", minProg: 55 },
    { id: "1.3", minProg: 55 },
    { id: "1.4", minProg: 55 },
  ],
  // Язык художественной литературы осмысляется на фоне остальных стилей.
  "2.5": [
    { id: "2.1", minProg: 55 },
    { id: "2.2", minProg: 55 },
    { id: "2.3", minProg: 55 },
    { id: "2.4", minProg: 55 },
  ],
  // Чужая речь — вершина пунктуации: нужны однородные, обособление,
  // вводные и сложное предложение.
  "3.8.9": [
    { id: "3.8.4", minProg: 50 },
    { id: "3.8.5", minProg: 50 },
    { id: "3.8.6", minProg: 50 },
    { id: "3.8.7", minProg: 50 },
  ],
  // Экология языка — обобщение всего раздела «Общие сведения о языке».
  "4.4": [
    { id: "4.1", minProg: 55 },
    { id: "4.2", minProg: 55 },
    { id: "4.3", minProg: 55 },
  ],
  // Публичное выступление опирается на речевую деятельность и этикет.
  "5.3": [
    { id: "5.1", minProg: 55 },
    { id: "5.2", minProg: 55 },
  ],

  // ── Содержательные цепочки внутри разделов ──
  // Аллитерацию/ассонанс не услышать без фонетического анализа.
  "3.2.2": [{ id: "3.2.1", minProg: 50 }],
  // Тропы распознаются через значение слова — лексический анализ.
  "3.3.2": [{ id: "3.3.1", minProg: 50 }],
  // Паронимы/плеоназм — нормы поверх того же лексического анализа.
  "3.3.3": [{ id: "3.3.1", minProg: 55 }],
  // Фигуры синтаксиса (инверсия, парцелляция) видны только после разбора.
  "3.6.2": [{ id: "3.6.1", minProg: 50 }],
  // Орфограммы корня требуют уверенного морфемного разбора.
  "3.7.2": [{ id: "3.4.1", minProg: 50 }],
  // Н/НН решается через часть речи — нужен морфологический анализ.
  "3.7.6": [{ id: "3.5.1", minProg: 55 }],
  // Обособление ставится там, где найден причастный/деепричастный оборот.
  "3.8.5": [{ id: "3.6.6", minProg: 55 }],
  // Знаки в ССП/СПП/БСП — после норм построения сложных предложений.
  "3.8.7": [{ id: "3.6.7", minProg: 50 }],
  // Комбинированная связь — надстройка над базовым сложным предложением.
  "3.8.8": [{ id: "3.8.7", minProg: 55 }],
};

/** hueShift для раздела (визуальный «регистр цвета» на Шпиле). */
const GROUP_HUE: Record<string, number> = {
  "1": -18,
  "2": -8,
  "3": 0,
  "4": 12,
  "5": 22,
};

/** Паттерн этажа зависит от вложенности кода (визуальный ритм). */
const PATTERNS = ["letters", "dots", "grid", "wave", "lines", "quill"] as const;

const GEOMS: TopicDef["geom"][] = ["disc", "hex", "torus", "slab", "core"];

export function buildRusFloors(): TopicDef[] {
  return FIPI_RU.map((t): TopicDef => {
    const rootGroup = t.code.split(".")[0];
    const depth = t.code.split(".").length; // 1.5→2, 3.7.6→3
    const isBoss = BOSSES.has(t.code);

    return {
      id: t.code,           // используем код ФИПИ как id этажа
      name: t.title,
      tag: t.keywords?.[0] ?? "тема",
      prog: 0,
      stab: 0,
      geom: isBoss ? "core" : GEOMS[depth % GEOMS.length],
      pattern: PATTERNS[depth % PATTERNS.length],
      hueShift: GROUP_HUE[rootGroup] ?? 0,
      boss: isBoss,
      ...(REQUIRES[t.code] ? { requires: REQUIRES[t.code] } : {}),
    };
  });
}
