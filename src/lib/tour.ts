const KEY = "egehack.tour.v1";

/** In-memory дубль флага: localStorage может быть недоступен (приватный режим,
 *  запрет куки) — без него markTourDone() молча терялся и автозапуск в
 *  page.tsx перезапускал тур бесконечно. */
let doneMem = false;

/** Тур уже пройден/пропущен? SSR-safe: на сервере — false. */
export function tourDone(): boolean {
  if (doneMem) return true;
  if (typeof window === "undefined") return false;
  try {
    return localStorage.getItem(KEY) === "1";
  } catch {
    return false;
  }
}

export function markTourDone(): void {
  doneMem = true; // сессия помнит, даже если storage откажет
  if (typeof window === "undefined") return;
  try {
    localStorage.setItem(KEY, "1");
  } catch {
    /* ignore */
  }
}
